#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t i;
	size_t s;

	if (src == '\0')
		return (0);
	s = ft_strlen(src);
	i = 0;
	if (dstsize == 0)
		return (s);
	while (src[i] != '\0' && i < (dstsize - 1))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (s);
}
