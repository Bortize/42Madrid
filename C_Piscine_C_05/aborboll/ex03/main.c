#include <stdio.h>

int	ft_recursive_power(int nb, int power)
{
	if (power < 0)
		return (0);
	else if (power > 0)
		return (nb * ft_recursive_power(nb, power - 1));
	else
		return (1);
}

int	main(void)
{
	printf("%i\n", ft_recursive_power(4, 4));
	return (0);
}
